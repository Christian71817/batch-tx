//主题工作函数
async function main() {
    //实例化web3对象
    const web3 = new Web3(rpc_url);

    //通过合约ABI与合约地址实例化合约对象
    const contractObj = new web3.eth.Contract(contractABI, contractAddress)
     //指定gas价格 具体设置可以参考区块链浏览器当前的gas价格，比最高gas高出一部分即可
    const gweiGasPrice = '0.05'
    const weiGasPrice = web3.utils.toWei(gweiGasPrice, 'gwei')

    //获取最新NONCE值
    const amount = '0' //向目标账户转账金额
    const valueEth = web3.utils.toWei(amount, 'ether')
    let n = 0;
    const total = Object.keys(accounts).length;
    
    //根据accounts对象的总数尽量 循环执行任务 直到任务结束
    for (let address in accounts) {
        
        //执行5秒间隔
        await sleep(5000)

        //私钥去除0x前缀
        let privateKey = accounts[address].slice(2)

        //获得当前地址的nonce
        const nonce = await web3.eth.getTransactionCount(address);

        //调用合约构造数据
        const transactionData = {
            nonce: nonce, // nonce值
            gasPrice: weiGasPrice, //gas价格 单位由Gwei转换为wei
            from: address, //发送方钱包地址
            to: contractAddress, //合约地址
            value: valueEth, //像目标账户转账的金额 单位eth
            data: contractObj.methods.mint().encodeABI() //调用mint方法的签名
        }

        //动态计算gas
        transactionData['gas'] = await web3.eth.estimateGas(transactionData); 
        
        //用私钥签名交易
        const signedTx = await web3.eth.accounts.signTransaction(transactionData, privateKey);
        
        //发送交易 广播到区块链
        const txReceipt = await web3.eth.sendSignedTransaction(signedTx.rawTransaction); 

        //区块广播日志
        console.log(txReceipt)

        //任务执行成功 打印成功信息
        if (txReceipt.status === true) {
            n++
            console.log(`地址【${address}】铸造创世NFT成功 任务执行第${n}条 共${total}条。`)
        } 
    }

}

main()





